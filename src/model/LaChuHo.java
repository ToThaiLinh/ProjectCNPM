package model;

public class LaChuHo {
	private String maHK;
	private String maNK;
	
	public LaChuHo() {
		super();
	}

	public LaChuHo(String maHK, String maNK) {
		super();
		this.maHK = maHK;
		this.maNK = maNK;
	}

	public String getMaHK() {
		return maHK;
	}

	public void setMaHK(String maHK) {
		this.maHK = maHK;
	}

	public String getMaNK() {
		return maNK;
	}

	public void setMaNK(String maNK) {
		this.maNK = maNK;
	}
	
	
}
